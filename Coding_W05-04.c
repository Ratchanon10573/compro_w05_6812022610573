#include <stdio.h>

// สร้างโครงสร้างข้อมูล struct
struct Person {
    char name; // ประกาศตัวแปร char สำหรับเก็บชื่อเล่น
    int age; // ประกาศตัวแปร int สำหรับเก็บอายุเป็นจำนวนเต็ม
    float height; // ประกาศตัวแปร float สำหรับเก็บส่วนสูงเป็นทศนิยม
    float weight; // ประกาศตัวแปร float สำหรับเก็บน้ำหนักเป็นทศนิยม
    char grade; // ประกาศตัวแปร char สำหรับเก็บเกรด
};
int main() { // ฟังก์ชันหลักของโปรแกรม
    struct Person people[3]; // ประกาศอาเรย์สำหรับเก็บข้อมูลของ 3 คน

    printf("=== Enter data for 3 people ===\n"); // ข้อความแนะนำให้ผู้ใช้ป้อนข้อมูล
    printf("Input format: Nickname Age Height Weight Grade\n"); // ข้อความแนะนำรูปแบบการป้อนข้อมูล
    printf("Example: A*18*175.5*65.5*A\n\n"); // ตัวอย่างการป้อนข้อมูล
    // รับข้อมูลของแต่ละคน
    for (int i = 0; i < 3; i++) {  // วนลูปรับข้อมูล 3 คน
        printf("Enter data for person %d: ", i + 1); // ข้อความให้ผู้ใช้ป้อนข้อมูล
        scanf(" %c*%d*%f*%f*%c", &people[i].name, &people[i].age, &people[i].height, &people[i].weight, &people[i].grade);
        // รับข้อมูลจากผู้ใช้โดยใช้ scanf ซึ่ง %c สำหรับตัวอักษร, %d สำหรับจำนวนเต็ม, %f สำหรับจำนวนทศนิยม
        // และใช้ & เพื่อส่งที่อยู่ของตัวแปร
        // รับข้อมูลชื่อเล่น, อายุ, ส่วนสูง, น้ำหนัก, เกรด
    }
    // แสดงผลในรูปแบบตาราง
    printf("\n+-------+-----+-----------+-----------+------------+\n"); // เส้นขอบบนของตาราง
    printf("| Name  | Age | Height(cm)| Weight(kg)| Grade Code |\n"); // หัวตาราง
    printf("+-------+-----+-----------+-----------+------------+\n"); // เส้นขอบกลางของตาราง

    for (int i = 0; i < 3; i++) { // วนลูปแสดงข้อมูลของแต่ละคน
        printf("|   %c   | %3d |   %6.1f  |   %6.1f  |     %c      |\n", // แสดงข้อมูลในรูปแบบตาราง
               people[i].name, // แสดงชื่อเล่น
               people[i].age, // แสดงอายุ
               people[i].height, // แสดงส่วนสูง
               people[i].weight, // แสดงน้ำหนัก
               people[i].grade); // แสดงเกรด
    }
    printf("+-------+-----+-----------+-----------+------------+\n"); // เส้นขอบล่างของตาราง
    return 0; // คืนค่า 0 เพื่อบอกว่าการทำงานสำเร็จ
}